import UIKit

public extension UIColor {
    static var snow: UIColor {
        return UIColor(red: 255/255, green: 252/255, blue: 249/255, alpha: 1.0)
    }
    static var paleTurquoise: UIColor {
        return UIColor(red: 177/255, green: 237/255, blue: 232/255, alpha: 1.0)
    }
    static var mayaBlue: UIColor {
        return UIColor(red: 108/255, green: 212/255, blue: 255/255, alpha: 1.0)
    }
    static var parrotPink: UIColor {
        return UIColor(red: 212/255, green: 150/255, blue: 167/255, alpha: 1.0)
    }
    static var blackCoral: UIColor {
        return UIColor(red: 93/255, green: 87/255, blue: 107/255, alpha: 1.0)
    }
    static var darkDarkGray: UIColor {
        return UIColor(red: 17/255, green: 17/255, blue: 17/255, alpha: 1.0)
    }
}
